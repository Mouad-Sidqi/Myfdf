#include "../Includes/fdf.h"

void	rotatepointiso(int *tab)
{
	int tmp;

	tmp = tab[0];
	tab[0] = ((tab[0]) * cos(0.46373398) - (tab[1]) * sin (0.46373398));
	tab[1] = ((tmp) * sin(0.46373398) + (tab[1]) * cos (0.46373398));
}