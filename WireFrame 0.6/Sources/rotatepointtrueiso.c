#include "../Includes/fdf.h"

void	rotatepointtrueiso(int *x, int *y)
{
	int tmp;

	tmp = *x;
	*x = ((*x) * cos(0.523599) - (*y) * sin (0.523599));
	*y = ((tmp) * sin(0.523599) + (*y) * cos (0.523599));
}
